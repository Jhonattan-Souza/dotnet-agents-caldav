namespace DotnetAgents.CalDav.Core.Tests.Unit.Internal.Ical;

public class TaskItemMapperTests
{
    [Fact]
    public void FromTodo_CommonFields_MapsCorrectly()
    {
        // Arrange
        var todo = new Todo
        {
            Uid = "test-uid-123",
            Summary = "Buy groceries",
            Description = "Milk, eggs, bread",
            Priority = 1,
            Status = "NEEDS-ACTION",
        };
        todo.Due = new CalDateTime(2025, 8, 15, 17, 0, 0);
        todo.DtStart = new CalDateTime(2025, 8, 1, 9, 0, 0);

        // Act
        var task = TaskItemMapper.FromTodo(todo, etag: "\"etag-123\"", href: "/calendars/user/task.ics");

        // Assert
        task.Uid.ShouldBe("test-uid-123");
        task.Summary.ShouldBe("Buy groceries");
        task.Description.ShouldBe("Milk, eggs, bread");
        task.Priority.ShouldBe(CalDavTaskPriority.High); // Priority 1 = High
        task.Status.ShouldBe(CalDavTaskStatus.NeedsAction);
        task.ETag.ShouldBe("\"etag-123\"");
        task.Href.ShouldBe("/calendars/user/task.ics");
        task.Due.ShouldNotBeNull();
        task.Start.ShouldNotBeNull();
    }

    [Fact]
    public void FromTodo_NullUid_DefaultsToAutoGeneratedUid()
    {
        // Note: iCal.NET's Todo auto-generates a UID when null is set
        var todo = new Todo
        {
            Summary = "No UID task",
        };

        var task = TaskItemMapper.FromTodo(todo);

        // iCal.NET auto-generates a UID, so it won't be empty string
        task.Uid.ShouldNotBeNullOrEmpty();
    }

