    [Fact]
    public void FromTodo_PriorityMapping_MapsCorrectly()
    {
        // Priority 0 = None
        var task0 = TaskItemMapper.FromTodo(new Todo { Uid = "1", Priority = 0 });
        task0.Priority.ShouldBe(CalDavTaskPriority.None);

        // Priority 1-4 = High
        var task1 = TaskItemMapper.FromTodo(new Todo { Uid = "2", Priority = 1 });
        task1.Priority.ShouldBe(CalDavTaskPriority.High);
        var task4 = TaskItemMapper.FromTodo(new Todo { Uid = "3", Priority = 4 });
        task4.Priority.ShouldBe(CalDavTaskPriority.High);

        // Priority 5 = Medium
        var task5 = TaskItemMapper.FromTodo(new Todo { Uid = "4", Priority = 5 });
        task5.Priority.ShouldBe(CalDavTaskPriority.Medium);

        // Priority 6-9 = Low
        var task9 = TaskItemMapper.FromTodo(new Todo { Uid = "5", Priority = 9 });
        task9.Priority.ShouldBe(CalDavTaskPriority.Low);

        // Out of range (negative or > 9) = None
        var taskNeg = TaskItemMapper.FromTodo(new Todo { Uid = "6", Priority = -1 });
        taskNeg.Priority.ShouldBe(CalDavTaskPriority.None);
        var task99 = TaskItemMapper.FromTodo(new Todo { Uid = "7", Priority = 99 });
        task99.Priority.ShouldBe(CalDavTaskPriority.None);
    }

    [Fact]
    public void FromTodo_StatusMapping_MapsCorrectly()
    {
        var needsAction = TaskItemMapper.FromTodo(new Todo { Uid = "1", Status = "NEEDS-ACTION" });
        needsAction.Status.ShouldBe(CalDavTaskStatus.NeedsAction);

        var inProcess = TaskItemMapper.FromTodo(new Todo { Uid = "2", Status = "IN-PROCESS" });
        inProcess.Status.ShouldBe(CalDavTaskStatus.InProcess);

        var completed = TaskItemMapper.FromTodo(new Todo { Uid = "3", Status = "COMPLETED" });
        completed.Status.ShouldBe(CalDavTaskStatus.Completed);

        var cancelled = TaskItemMapper.FromTodo(new Todo { Uid = "4", Status = "CANCELLED" });
        cancelled.Status.ShouldBe(CalDavTaskStatus.Cancelled);

        // null status defaults to NeedsAction
        var nullStatus = TaskItemMapper.FromTodo(new Todo { Uid = "5" });
        nullStatus.Status.ShouldBe(CalDavTaskStatus.NeedsAction);

        // Unknown status defaults to NeedsAction
        var unknownStatus = TaskItemMapper.FromTodo(new Todo { Uid = "6", Status = "UNKNOWN" });
        unknownStatus.Status.ShouldBe(CalDavTaskStatus.NeedsAction);
    }

    [Fact]
