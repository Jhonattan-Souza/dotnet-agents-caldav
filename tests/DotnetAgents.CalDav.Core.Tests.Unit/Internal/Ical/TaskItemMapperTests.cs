    [Fact]
    public void ToICalText_AndBack_RoundTripPreservesCommonFields()
    {
        // Arrange
        var original = new TaskItem
        {
            Uid = "roundtrip-uid",
            Summary = "Round trip task",
            Description = "Test description",
Priority = CalDavTaskPriority.High,
        Status = CalDavTaskStatus.NeedsAction,
            Due = new DateTimeOffset(2025, 8, 15, 17, 0, 0, TimeSpan.Zero),
            Categories = ["Work"],
        };

        // Act - serialize to iCal text
        var icalText = TaskItemMapper.ToICalText(original);

        // Assert - should be valid iCalendar
        icalText.ShouldNotBeNullOrEmpty();
        icalText.ShouldContain("BEGIN:VCALENDAR");
        icalText.ShouldContain("BEGIN:VTODO");
        icalText.ShouldContain("SUMMARY:Round trip task");

        // Act - parse it back
        var parsed = TaskItemMapper.FromICalText(icalText, etag: "etag-rt");

        // Assert - round trip preserves key fields
        parsed.Count.ShouldBe(1);
        parsed[0].Uid.ShouldBe("roundtrip-uid");
        parsed[0].Summary.ShouldBe("Round trip task");
        parsed[0].Priority.ShouldBe(CalDavTaskPriority.High);
        parsed[0].ETag.ShouldBe("etag-rt");
    }

